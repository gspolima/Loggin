CREATE TABLE USUARIOS (
	ID INT AUTO_INCREMENT NOT NULL UNIQUE,
    LOGIN VARCHAR(25) NOT NULL UNIQUE,
    EMAIL VARCHAR(60) NOT NULL UNIQUE,
    CPF VARCHAR(11) NOT NULL UNIQUE,
    DATA_NASCIMENTO DATE,
    
    PRIMARY KEY(ID),
    INDEX PK_IDX(ID),
    INDEX CPF_IDX(CPF),
    INDEX LOGIN_IDX(LOGIN)
);

